//Creamos las variables necesarias para el cálculo de las propiedades de un círculo.
var radio=3;
var circunferencia= 2*Math.PI*radio;
var area= Math.PI*Math.pow(radio,2);

//Mostramos por pantalla el resultado.
document.write("The circumference is "+circunferencia+"<br>");
document.write("The area is "+area)
