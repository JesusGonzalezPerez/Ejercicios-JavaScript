//Creamos dos variables para almacenar dos números introducidos por teclado.
var num1=parseInt(prompt("Introduce el valor para la variable 1"));
var num2=parseInt(prompt("Introduce el valor para la variable 2"));

//Creamos y asignamos valores a cada operacion matemática además de mostrar su resultado.
var suma=num1+num2;
document.write("El valor de la suma es: "+suma+"<br>");

var resta=num1-num2;
document.write("El valor de la resta es: "+resta+"<br>");

var mult=num1*num2;
document.write("El valor de la multiplicación es: "+mult+"<br>");

var div=num1/num2;
document.write("El valor de la división es: "+div+"<br>");